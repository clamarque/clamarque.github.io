const LANG_KEY = "lang"
const SUPPORTED = ["en", "fr"]

function detectInitialLang() {
  const saved = localStorage.getItem(LANG_KEY)
  if (saved && SUPPORTED.includes(saved)) return saved
  const browser = (navigator.language || "en").slice(0, 2).toLowerCase()
  return SUPPORTED.includes(browser) ? browser : "en"
}

function applyAge(root) {
  const birthYear = 1991
  const age = new Date().getFullYear() - birthYear
  root.querySelectorAll("#age").forEach((el) => (el.textContent = age))
  const years = new Date().getFullYear() - 2016
  root
    .querySelectorAll("#years-experience")
    .forEach((el) => (el.textContent = years))
  const footerYear = root.querySelector("#footer-year")
  if (footerYear) footerYear.textContent = new Date().getFullYear()
}

function applyLang(lang) {
  const dict = I18N[lang] || I18N.en
  document.documentElement.setAttribute("lang", lang)

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n")
    if (dict[key] != null) el.textContent = dict[key]
  })

  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    const key = el.getAttribute("data-i18n-html")
    if (dict[key] != null) el.innerHTML = dict[key]
  })

  applyAge(document)

  const toggle = document.getElementById("lang-toggle")
  if (toggle) {
    const label = toggle.querySelector(".lang-toggle__current")
    if (label) label.textContent = lang.toUpperCase()
    toggle.setAttribute(
      "aria-label",
      lang === "fr" ? "Switch to English" : "Passer en français"
    )
  }

  try {
    localStorage.setItem(LANG_KEY, lang)
  } catch {}
}

function initI18n() {
  const initial = detectInitialLang()
  applyLang(initial)

  const toggle = document.getElementById("lang-toggle")
  if (toggle) {
    toggle.addEventListener("click", () => {
      const current = localStorage.getItem(LANG_KEY) || initial
      const next = current === "fr" ? "en" : "fr"
      applyLang(next)
    })
  }
}

document.addEventListener("DOMContentLoaded", initI18n)
